            ", labelName = " + labelName +
            ", sortNum = " + sortNum +
            ", categoryId = " + categoryId +
            ", createdBy = " + createdBy +
            ", createdTime = " + createdTime +
            ", updateBy = " + updateBy +
            ", updateTime = " + updateTime +
            ", isDeleted = " + isDeleted +
        "}";
